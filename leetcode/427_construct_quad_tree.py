    def go(self, size, y, x):  # list of values replaced with a set
        values = set(
            self.grid[i][j] for i in range(y, y + size) for j in range(x, x + size)
        )
        if len(values) == 1:
            if values.pop() == 0:
                return Node(
                    val=False,
                    isLeaf=True,
                    topLeft=None,
                    topRight=None,
                    bottomLeft=None,
                    bottomRight=None,
                )
            else:
                return Node(
                    val=True,
                    isLeaf=True,
                    topLeft=None,
                    topRight=None,
                    bottomLeft=None,
                    bottomRight=None,
                )
        else:
            s = size // 2
            return Node(
                val=None,
                isLeaf=False,
                topLeft=self.go(s, y, x),
                topRight=self.go(s, y, x + s),
                bottomLeft=self.go(s, y + s, x),
                bottomRight=self.go(s, y + s, x + s),
            )

    def construct(self, grid):
        """
        :type grid: List[List[int]]
        :rtype: Node
        """
        size = len(grid)
        self.grid = grid
        return self.go(size, 0, 0)


